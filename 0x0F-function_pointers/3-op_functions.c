#include "3-calc.h"

/**
 * op_add - calculates the sum of two numbers
 * @a: The first number
 * @b: The second number
 *
 * Return: The sum of the two numbers
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - calculates the difference of two numbers
 * @a: The first number
 * @b: The second number
 *
 * Return: The difference of the two numbers
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_add - calculates the product of two numbers
 * @a: The first number
 * @b: The second number
 *
 * Return: The product of the two numbers
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_add - calculates the quotient of two numbers
 * @a: The first number
 * @b: The second number
 *
 * Return: The quotient of the two numbers
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_add - calculates the modulus of two numbers
 * @a: The first number
 * @b: The second number
 *
 * Return: The modulus of the two numbers
 */
int op_mod(int a, int b)
{
	return (a % b);
}
