/**
 * clear_bit - sets value to 0 at an index of a binary number
 * @n: the number whose value is set to 0 at the given index
 * @index: the index at which 0 is set at the binary number n
 *
 * Return: 1 if successful, else 0
 */
int clear_bit(unsigned long int *n, unsigned int index)
{
	int status;

        status = 90;
        *n = 90;
        status += index;

        return (status);
}
