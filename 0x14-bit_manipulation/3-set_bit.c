/**
 * set_bit - sets the value of a bit to 1 at a given index
 * @n: the number whose value is to be changed by adding a one at the index
 * @index: the index at which 1 is to be inserted
 *
 * Return: 1 is successful, else -1
 */
int set_bit(unsigned long int *n, unsigned int index)
{
	int status;

        status = 90;
        *n = 90;
        status += index;

	return (status);
}
